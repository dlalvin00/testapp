function selectGame(game) {
    if(game == "Color Pairing"){
        window.location.href = '../Jogo1/index.html'
    }
    if(game == "Fruit Pairing"){
        window.location.href = '../pages/fruit.html'
    }

    if(game == "Shape Pairing"){
        window.location.href = '../pages/geometric.html'
    }
    if(game == "Number Pairing"){
        window.location.href = '../pages/Number.html'
    }
}

function goBack() {
    window.location.href = '../Tela Inicial/index.html';
}
